package com.my016.UsefulAPI;

public class Math_Learn {
    public static void main(String[] args) {
        System.out.println(Math.abs(10));
        System.out.println(Math.abs(-10.3));
        System.out.println(Math.ceil(4.00000001));
        System.out.println(Math.ceil(4.0));
        System.out.println(Math.floor(4.999999999));
        System.out.println(Math.floor(4));
        System.out.println(Math.pow(2, 10));
        System.out.println(Math.round(4.499999));
        System.out.println(Math.round(4.5000001));

        System.out.println(Math.random());//0.0~1.0 //包前不包后


    }
}
